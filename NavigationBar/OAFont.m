#import "OAFont.h"

UIFont *fontDefaultTitleFont(CGFloat value){
    return [UIFont fontWithName:@"Helvetica-Bold" size:value];
}

UIFont *fontDefaultContentFont(CGFloat value){
    return [UIFont fontWithName:@"Helvetica-Light" size:value];
}
